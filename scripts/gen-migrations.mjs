// Inlines the SQL migration files into a TS module so the compiled single-file
// binary (which has no filesystem access to src/store/migrations) can run them.
// Runs in the build pipeline before tsc / bun build.
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const migDir = join(here, "..", "src", "store", "migrations");
const out = join(here, "..", "src", "store", "migrations.generated.ts");

const files = readdirSync(migDir)
  .filter((f) => f.endsWith(".sql"))
  .sort();

const entries = files.map((file) => {
  const version = Number(file.split("_")[0]);
  const sql = readFileSync(join(migDir, file), "utf8");
  return { version, file, sql };
});

const body = `// AUTO-GENERATED by scripts/gen-migrations.mjs — do not edit by hand.
// SQL migrations inlined so the compiled binary needs no filesystem access.
export interface Migration {
  version: number;
  file: string;
  sql: string;
}

export const MIGRATIONS: Migration[] = ${JSON.stringify(entries, null, 2)};
`;

writeFileSync(out, body);
console.log(`gen-migrations: wrote ${entries.length} migrations → ${out}`);
